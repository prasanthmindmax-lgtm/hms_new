                    <li>After the procedure of egg retrieval, the <b>Eggs are Fertilizedwith Sperm</b> in an IVF lab. </li>
                    <li>The next step after the formation of embryo is the culturing of those embryos for 3-5 days known as <b>Blastocyst Culture</b>.  </li>
                    <li>After the culturing of embryos, they are transferred back to the uterus to implant called <b>Implantation of Embryo</b>. </li>
